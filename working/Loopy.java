public class Loopy {
 public static void main (String[] args) {
	 int x = 1;
	 System.out.println("Before the Loop");
	 while (x < 4) {
		 System.out.println("In the loop");
		 System.out.print("Value of x is ");
		 System.out.println(x);
		 x = x + 1;
	 }
	 System.out.println("This is after the loop");
 }
}
